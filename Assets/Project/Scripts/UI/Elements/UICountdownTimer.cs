
using System;
using UnityEngine;
using TMPro;

public class UICountdownTimer : MonoBehaviour
{
    /*################################################################################
        Variables
    ################################################################################*/
    /*--------------------------------------------------------------------------------
        References
    --------------------------------------------------------------------------------*/
    [Header("References")]
    public TextMeshProUGUI textTime;

    /*################################################################################
        Functions: public
    ################################################################################*/
    /*--------------------------------------------------------------------------------
        SetTime
    --------------------------------------------------------------------------------*/
    public void SetTime(TimeSpan currentTime)
    {
        textTime.text = currentTime.ToString(@"ss");
    }
}
