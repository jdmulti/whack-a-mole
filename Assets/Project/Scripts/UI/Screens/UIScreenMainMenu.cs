using UnityEngine;

public class UIScreenMainMenu : UIScreen
{
    /*################################################################################
        Variables
    ################################################################################*/
    /*--------------------------------------------------------------------------------
        References
    --------------------------------------------------------------------------------*/
    [Header("References")]
    public UIButton buttonStart;

    /*--------------------------------------------------------------------------------
        Settings
    --------------------------------------------------------------------------------*/
    [Header("Settings")]
    public StateMachine.State nextScreen;

    /*################################################################################
        Functions: public
    ################################################################################*/
    /*--------------------------------------------------------------------------------
        Init
    --------------------------------------------------------------------------------*/
    /// <summary>
    /// Initialize screen.
    /// </summary>
    public override void Init()
    {
        base.Init();
        buttonStart.PointerDown += ButtonStart_PointerDown;
    }

    /*################################################################################
        Functions: on event
    ################################################################################*/
    /*--------------------------------------------------------------------------------
        ButtonStart_PointerDown
    --------------------------------------------------------------------------------*/
    /// <summary>
    /// Executed whenever start button has been pressed.
    /// </summary>
    /// <param name="uiButton">Button which has been pressed.</param>
    private void ButtonStart_PointerDown(UIButton uiButton)
    {
        StateMachine.SetState(nextScreen);
    }
}
